def area_of_circle(radius):
    circle = 3.14 * (radius**2)
    return circle

def area_of_tri(base, height):
    triangle = .5 * base * height
    return triangle

def area_of_square(side):
    square = side**2
    return square

def area_of_rectangle(length, width):
    rectangle = length * width
    return rectangle



